package ge.mziuri.model;

public class ClassGroup {
    private int id;
    private String name;

    public ClassGroup() {
    }

    public ClassGroup(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}